#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	i = 0;
	d = (unsigned char *) dst;
	s = (unsigned char *) src;
	if (n < 1)
		return (dst);
	while (i < n && d[i])
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
